package com.smart.fast.learning.model.frame.domain;

public class PointFor2D {

    private Integer x;

    private Integer y;

    public PointFor2D(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
}
